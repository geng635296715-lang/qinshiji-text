def build_rows(source: list[tuple[str, str, str, str]], volume_name: str) -> list[list[str]]:
    rows: list[list[str]] = []
    for index, (name, attr, short_tag, brief) in enumerate(source, start=1):
        full_note = (
            f"{volume_name}第{index}条，神煞名为“{name}”。属性归类为“{attr}”。"
            f"{ATTRIBUTE_EXPLAIN[attr]} {brief} {CATEGORY_HINT[attr]} "
            "我们的内部规则库里，这条神煞优先作为辅助断点使用，不脱离原局旺衰与岁运单独下结论。"
        )
        short_note = f"{short_tag}：{brief}"
        rows.append([name, attr, full_note, short_note])
    return rows


def fill_sheet(worksheet, title: str, rows: list[list[str]]) -> None:
    headers = ["神煞名字", "神煞属性", "神煞完全注释", "神煞简介注释"]
    worksheet.append(headers)
    for row in rows:
        worksheet.append(row)

    thin = Side(style="thin", color="6E5629")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)
    header_fill = PatternFill("solid", fgColor="2A2116")
    header_font = Font(color="F3D38A", bold=True, size=12)
    text_font = Font(color="1F1A15", size=11)
    center = Alignment(horizontal="center", vertical="center", wrap_text=True)
    left = Alignment(horizontal="left", vertical="top", wrap_text=True)
    attr_fills = {
        "吉": PatternFill("solid", fgColor="EEE3FF"),
        "中": PatternFill("solid", fgColor="F5F5F5"),
        "凶": PatternFill("solid", fgColor="FFE3E3"),
    }

    worksheet.freeze_panes = "A2"
    worksheet.sheet_view.showGridLines = False
    worksheet.row_dimensions[1].height = 28

    for col_index, header in enumerate(headers, start=1):
        cell = worksheet.cell(1, col_index)
        cell.value = f"{title}｜{header}" if col_index == 1 else header
        cell.fill = header_fill
        cell.font = header_font
        cell.border = border
        cell.alignment = center

    widths = {1: 18, 2: 12, 3: 78, 4: 42}
    for col_index, width in widths.items():
        worksheet.column_dimensions[get_column_letter(col_index)].width = width

    for row_index in range(2, worksheet.max_row + 1):
        worksheet.row_dimensions[row_index].height = 72
        attr_value = worksheet.cell(row_index, 2).value
        for col_index in range(1, 5):
            cell = worksheet.cell(row_index, col_index)
            cell.border = border
            cell.font = text_font
            cell.alignment = center if col_index in (1, 2) else left
            if col_index == 2 and attr_value in attr_fills:
                cell.fill = attr_fills[attr_value]


def main() -> Path:
    assert len(COMMON_SHENSHA) == 54
    assert len(UNCOMMON_SHENSHA) == 54
    all_names = [item[0] for item in COMMON_SHENSHA + UNCOMMON_SHENSHA]
    assert len(all_names) == 108
    assert len(set(all_names)) == 108

    workbook = Workbook()
    top_sheet = workbook.active
    top_sheet.title = "上卷-常用神煞"
    bottom_sheet = workbook.create_sheet("下卷-不常用神煞")

    fill_sheet(top_sheet, "上卷-常用神煞", build_rows(COMMON_SHENSHA, "上卷"))
    fill_sheet(bottom_sheet, "下卷-不常用神煞", build_rows(UNCOMMON_SHENSHA, "下卷"))

    output_dir = Path("deliverables")
    output_dir.mkdir(exist_ok=True)
    output_path = output_dir / "青筮记_108神煞规则表_上卷下卷.xlsx"
    workbook.save(output_path)
    return output_path.resolve()


if __name__ == "__main__":
    print(main())
